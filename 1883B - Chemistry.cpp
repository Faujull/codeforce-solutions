#include <bits/stdc++.h>
using namespace std;

int main() {
    long long t;
    cin >> t;
    while(t--)
    {
        long long n,k;
        cin >> n >> k;
        string s;
        cin >> s;
        vector<int>all(26,0);
        for(int i=0; i<n; i++)
        {
            all[s[i]-'a']++;//'c' - 'a' = 2
                            //'m' - 'a' = 12
        }
        long long odd=0;
        for(int i=0; i<26; i++)
        {
            if(all[i]%2!=0)
            odd++;
        }
        if(odd>k+1)
        {
            cout << "NO" << endl;
        }
        else
        {
            cout << "YES" << endl;
        }
    }
    return 0;
}
