#include <bits/stdc++.h>
using namespace std;

vector<int> a(999999 + 1);

void pre() {
    a[0] = 0;
    for (int i = 1; i <= 999999; i++) {
        int x = i;
        int sum = 0;
        while (x) {
            if ((x % 10) != 0)
                sum++;
            x /= 10;
        }
        if (sum == 1)
            a[i] = a[i - 1] + 1;
        else
            a[i] = a[i - 1];
    }
}

int main()
{
    pre();
    int t;
    cin >> t;
    while (t--)
    {
        int n;
        cin >> n;
        cout << a[n] << endl;
    }

    return 0;
}
