#include <bits/stdc++.h>
using namespace std;

int main() {
    long long n;
    string s,a;
    cin >> n >> s;
    int ans=0;
    for(int i=0; i<n; i++)
    {
        int c=0;
        for(int j=0; j<n; j++)
        {
            if(s[i]==s[j] && s[i+1]==s[j+1])
            {
                c++;
            }
        }
        if(ans<c)
        {
            ans = max(ans,c);
            a = string(1,s[i])+string(1,s[i+1]);
        }
    }
    cout << a << endl;
    return 0;
}
