#include <bits/stdc++.h>
using namespace std;

int main() {
    long long n,t,sum=0,x=0,ans=0;
    cin >> n >> t;
    vector<long long>a(n);
    for(int i=0; i<n; i++)
    {
        cin >> a[i];
    }
    for(int i=0; i<n; i++)
    {
        sum+=a[i];
        while(sum>t)
        {
            sum-=a[x];
            x++;
        }
        ans=max(ans,i-x+1);
    }
    cout << ans << endl;
    return 0;
}
