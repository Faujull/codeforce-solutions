#include <bits/stdc++.h>
using namespace std;

int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        long long n, count = 0;
        cin >> n;
        string s;
        cin >> s;

        int i = 0, j = n - 1;
        while (i < j)
        {
            if (s[i] == s[j])
            {
                break;
            }
            else
            {
                count += 2;
                i++;
                j--;
            }
        }

        cout << n - count << endl;
    }
    return 0;
}

