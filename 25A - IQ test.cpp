#include <bits/stdc++.h>
using namespace std;

int main() {
    long long n,even=0,odd=0,x=0,y=0;
    cin >> n;
    vector<long long>a(n);
    for(int i=0; i<n; i++)
    {
        cin >> a[i];
        if(a[i]%2==0)
        {
            even ++;
            x=i+1;
        }
        else
        {
            odd++;
            y=i+1;
        }
    }
    if(even>odd)
    {
        cout << y << endl;
    }
    else
    {
        cout << x << endl;
    }
    return 0;
}
